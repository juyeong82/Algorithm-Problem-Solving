# 문제: 백준 1546 (평균)
# 날짜: 2026-01-29
# 시간 복잡도:** O(N) - N이 1,000이므로 1초 내 충분히 가능 [1초당 2000만번 연산 가능]
# 틀린 이유: 

'''
세준이는 기말고사를 망쳤다. 세준이는 점수를 조작해서 집에 가져가기로 했다. 일단 세준이는 자기 점수 중에 최댓값을 골랐다. 
이 값을 M이라고 한다. 그리고 나서 모든 점수를 점수/M*100으로 고쳤다.
예를 들어, 세준이의 최고점이 70이고, 수학점수가 50이었으면 수학점수는 50/70*100이 되어 71.43점이 된다.
세준이의 성적을 위의 방법대로 새로 계산했을 때, 새로운 평균을 구하는 프로그램을 작성하시오.

입력
첫째 줄에 시험 본 과목의 개수 N이 주어진다. 
이 값은 1000보다 작거나 같다. 둘째 줄에 세준이의 현재 성적이 주어진다. 
이 값은 100보다 작거나 같은 음이 아닌 정수이고, 적어도 하나의 값은 0보다 크다.
'''
# A. 문제분석

# 1. 시험 본 과목의 개수 n을 저장한다.
# 2. list split map을 이용해 세준이의 현재 성적을 저장한다. 
# 3. 최대값을 계산한다.
# 4. 점수별로 최대값 기준 점수로 바꾼다.
# 5. 평균을 구한다.

# 솔루션1
n = input()

scores = list(map(int, input().split()))
max = scores[0]
sum = 0

for i in scores[1:]:
    if i > max:
        max = i

for i in scores:
    sum += i
    
answer = sum/max*100/int(n)
print(answer)

# 솔루션2
import sys

# 1. 입력 받기
n = int(sys.stdin.readline())
scores = list(map(int, sys.stdin.readline().split()))

# 2. 문제 분석을 통해 얻은 효율적인 로직 적용
max_val = max(scores) # 최댓값 찾기
total_sum = sum(scores) # 총합 구하기

# 3. 공식 적용하여 결과 도출
# 파이썬은 정수 나누기(/) 시 자동으로 실수형(float)을 반환함
print(total_sum * 100 / max_val / n)